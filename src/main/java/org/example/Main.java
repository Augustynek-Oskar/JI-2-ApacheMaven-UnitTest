package org.example;

public class Main {

    public static int add(int numA, int numB){
        return numA + numB;
    }

    public static int subtract(int numA, int numB){
        return numA - numB;
    }

    public static int multiply (int numA, int numB){
        return numA * numB;
    }

    public static float divide (float numA, float numB){
        return numA / numB;
    }

}